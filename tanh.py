import numpy as np
import matplotlib.pyplot as plt

x = np.linspace(-10, 10, 400)
y = np.tanh(x)

plt.figure()
plt.plot(x, y)
plt.xlabel("Input")
plt.ylabel("Output")
plt.title("Tanh Activation Function")
plt.show()